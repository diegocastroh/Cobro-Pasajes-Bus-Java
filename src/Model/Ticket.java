package Model;

/**
 *
 * @author lol_2
 */
public class Ticket {
    private int id;
    private int ruta_id;
    private double precio;
    private String tipo;

    public Ticket() {
    }

    public Ticket(int id, int ruta_id, double precio, String tipo) {
        this.id = id;
        this.ruta_id = ruta_id;
        this.precio = precio;
        this.tipo = tipo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRuta_id() {
        return ruta_id;
    }

    public void setRuta_id(int ruta_id) {
        this.ruta_id = ruta_id;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
}
